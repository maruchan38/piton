print("introduce los numeros a comparar: \n")

num_1 = int(input("introduce el primer nuemero : "))

num_2 = int(input("introduce el segundo numero : "))

print("\n los numeros a comparar son: " , num_1 , " y " , num_2 , "\n")

if num_1 == num_2:
    print("es igual...")

if num_1 != num_2:
    print("es diferente...")

if num_1 > num_2:
    print("es mayor...")

if num_1 < num_2:
    print("es menor...")

if num_1 >= num_2:
    print("es mayor o igual...")

if num_1 <= num_2:
    print("es menor o igual...")

    
